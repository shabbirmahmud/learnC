#include <stdio.h>
int main()
{
    float a=5.5678,b=7.6578;
    printf("a+b= %f\n",a+b);
    printf("a-b= %f\n",a-b);
    printf("a*b= %f\n",a*b);
    printf("a/b= %f\n",a/b);
    printf("a==b= %d\n",a==b);
    printf("a||b= %d\n",a||b);
    printf("a&&b= %d\n",a&&b);
    printf("b!= %d\n",!b);
    return 0;
}
